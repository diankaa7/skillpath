// Функция для получения случайных вопросов
function getRandomQuestions(count = 5) {
  const allQuestions = chatbotExampleQuestions.flatMap(category => 
    category.questions.map(q => ({ question: q, category: category.category }))
  );
  
  const shuffled = allQuestions.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

// Функция для получения вопросов по категории
function getQuestionsByCategory(category) {
  const categoryData = chatbotExampleQuestions.find(cat => cat.category === category);
  return categoryData ? categoryData.questions : [];
}
